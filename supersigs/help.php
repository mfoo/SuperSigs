					<p>If you are using a class that isn't from Team Fortress 2, you will need to make sure that a folder exists in the <i>images</i> directory with a few images inside of it before you can use that class.</p>
					<p><b>I want to use a font that's not on the list!</b></p>
					<p>If you have a TrueType (ends with .ttf) font that you want to use talk to you server admin and get them to upload it to the <i>fonts</i> directory.</p>
